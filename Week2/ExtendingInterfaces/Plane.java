public class Plane implements FlyingVehicle {
    public void drive() {
        System.out.println("Plane is driving!");
    }

    public void stop() {
        System.out.println("Plane is stopping!");
    }

    public void fly() {
        System.out.println("Plane is flying!");
    }

    public void land() {
        System.out.println("Plane is landing!");
    }
}
